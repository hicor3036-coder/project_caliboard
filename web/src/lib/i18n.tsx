export type Dict = typeof ko

const dict: Record<Lang, Dict> = { ko, en }

// ─── Context ───

interface I18nContextValue {
  t: Dict
  lang: Lang
  setLang: (l: Lang) => void
}

const I18nContext = createContext<I18nContextValue>({
  t: ko,
  lang: 'ko',
  setLang: () => {},
})

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>('ko')
  const [mounted, setMounted] = useState(false)

  useEffect(() => {
    const stored = localStorage.getItem('lang') as Lang | null
    if (stored && dict[stored]) setLangState(stored)
    setMounted(true)
  }, [])

  const setLang = (l: Lang) => {
    setLangState(l)
    localStorage.setItem('lang', l)
  }

  // SSR hydration mismatch 방지: 마운트 전에는 ko 기본값 사용
  const t = mounted ? dict[lang] : ko

  return (
    <I18nContext.Provider value={{ t, lang: mounted ? lang : 'ko', setLang }}>
      {children}
    </I18nContext.Provider>
  )
}

export function useT() {
  return useContext(I18nContext)
}
