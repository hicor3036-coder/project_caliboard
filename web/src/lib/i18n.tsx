/** 플레이스홀더 포맷: fmt('총 {0}건', 123) → '총 123건' */
export function fmt(template: string, ...args: (string | number)[]): string {
  return template.replace(/\{(\d+)\}/g, (_, i) => String(args[+i]))
}

